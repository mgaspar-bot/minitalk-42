#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*first;
	t_list	*new;

	first = NULL;
	while (lst)
	{
		new = ft_lstnew((t_list *)(*f)(lst->content));
		if (!new)
			ft_lstdelone(new, del);
		ft_lstadd_back(&first, new);
		lst = lst->next;
	}
	return (first);
}
